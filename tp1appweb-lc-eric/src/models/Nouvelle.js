class Nouvelle{

    date;
    titre;
    noReference;
    image;
    texte;
    resume;


    constructor(date, titre, noReference, image, texte, resume) {
        this.date = date;
        this.titre = titre;
        this.noReference = noReference;
        this.image = image;
        this.texte = texte;
        this.resume = resume;
    }


}